defmodule Klaviyo.DeviceMetadata do
  @moduledoc """
  Provides struct and type for a DeviceMetadata
  """

  @type t :: %__MODULE__{
          app_build: String.t() | nil,
          app_id: String.t() | nil,
          app_name: String.t() | nil,
          app_version: String.t() | nil,
          device_id: String.t() | nil,
          device_model: String.t() | nil,
          environment: String.t() | nil,
          klaviyo_sdk: String.t() | nil,
          manufacturer: String.t() | nil,
          os_name: String.t() | nil,
          os_version: String.t() | nil,
          sdk_version: String.t() | nil
        }

  defstruct [
    :app_build,
    :app_id,
    :app_name,
    :app_version,
    :device_id,
    :device_model,
    :environment,
    :klaviyo_sdk,
    :manufacturer,
    :os_name,
    :os_version,
    :sdk_version
  ]

  @doc false
  @spec __fields__(atom) :: keyword
  def __fields__(type \\ :t)

  def __fields__(:t) do
    [
      app_build: :string,
      app_id: :string,
      app_name: :string,
      app_version: :string,
      device_id: :string,
      device_model: :string,
      environment: {:enum, ["debug", "release"]},
      klaviyo_sdk: {:enum, ["android", "swift"]},
      manufacturer: :string,
      os_name: {:enum, ["android", "ios", "ipados", "macos", "tvos"]},
      os_version: :string,
      sdk_version: :string
    ]
  end
end
