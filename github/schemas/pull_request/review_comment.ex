defmodule GitHub.PullRequest.ReviewComment do
  @moduledoc """
  Provides struct and type for a PullRequest.ReviewComment
  """
  use GitHub.Encoder

  @type t :: %__MODULE__{
          __info__: map,
          _links: GitHub.PullRequest.ReviewCommentLinks.t(),
          author_association: String.t(),
          body: String.t(),
          body_html: String.t() | nil,
          body_text: String.t() | nil,
          commit_id: String.t(),
          created_at: DateTime.t(),
          diff_hunk: String.t(),
          html_url: String.t(),
          id: integer,
          in_reply_to_id: integer | nil,
          line: integer | nil,
          node_id: String.t(),
          original_commit_id: String.t(),
          original_line: integer | nil,
          original_position: integer | nil,
          original_start_line: integer | nil,
          path: String.t(),
          position: integer | nil,
          pull_request_review_id: integer | nil,
          pull_request_url: String.t(),
          reactions: GitHub.Reaction.Rollup.t() | nil,
          side: String.t() | nil,
          start_line: integer | nil,
          start_side: String.t() | nil,
          subject_type: String.t() | nil,
          updated_at: DateTime.t(),
          url: String.t(),
          user: GitHub.User.simple()
        }

  defstruct [
    :__info__,
    :_links,
    :author_association,
    :body,
    :body_html,
    :body_text,
    :commit_id,
    :created_at,
    :diff_hunk,
    :html_url,
    :id,
    :in_reply_to_id,
    :line,
    :node_id,
    :original_commit_id,
    :original_line,
    :original_position,
    :original_start_line,
    :path,
    :position,
    :pull_request_review_id,
    :pull_request_url,
    :reactions,
    :side,
    :start_line,
    :start_side,
    :subject_type,
    :updated_at,
    :url,
    :user
  ]

  @doc false
  @spec __fields__(atom) :: keyword
  def __fields__(type \\ :t)

  def __fields__(:t) do
    [
      _links: {GitHub.PullRequest.ReviewCommentLinks, :t},
      author_association:
        {:enum,
         [
           "COLLABORATOR",
           "CONTRIBUTOR",
           "FIRST_TIMER",
           "FIRST_TIME_CONTRIBUTOR",
           "MANNEQUIN",
           "MEMBER",
           "NONE",
           "OWNER"
         ]},
      body: :string,
      body_html: :string,
      body_text: :string,
      commit_id: :string,
      created_at: {:string, "date-time"},
      diff_hunk: :string,
      html_url: {:string, "uri"},
      id: :integer,
      in_reply_to_id: :integer,
      line: :integer,
      node_id: :string,
      original_commit_id: :string,
      original_line: :integer,
      original_position: :integer,
      original_start_line: {:union, [:integer, :null]},
      path: :string,
      position: :integer,
      pull_request_review_id: {:union, [:integer, :null]},
      pull_request_url: {:string, "uri"},
      reactions: {GitHub.Reaction.Rollup, :t},
      side: {:enum, ["LEFT", "RIGHT"]},
      start_line: {:union, [:integer, :null]},
      start_side: {:enum, ["LEFT", "RIGHT", nil]},
      subject_type: {:enum, ["line", "file"]},
      updated_at: {:string, "date-time"},
      url: :string,
      user: {GitHub.User, :simple}
    ]
  end
end
